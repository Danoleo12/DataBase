drop database if exists Teaching;
create database Teaching;

use Teaching;

drop table if exists Student;
create table Student (
	student_id int unique primary key,
    first_name varchar(100),
    last_name varchar(100),
    gender varchar(10),
    cgpa decimal (3, 2),
    admission_date date,
    email varchar(100),
    constraint email_check check (Email LIKE '%@%'),
	extra_curricular_activities varchar(100),
    part_time_job boolean,
    weekly_self_study_hours int,
    career_aspiration varchar(100)
);

drop table if exists Assignment;
create table Assignment(
	assignment_number int,
    course_code varchar(20),
    due_date date,
    primary key (assignment_number, course_code)
);

drop table if exists Instructor;
create table Instructor(
	instructor_id int unique primary key,
    first_name varchar(100),
    last_name varchar (100),
    email  varchar(100) unique,
    gender varchar(100)
);

drop table if exists course;
create table Course(
	course_code varchar(20) unique primary key, -- Eg: CSCI_1300
    section int,
    capacity int,
    rating decimal (2, 1), -- Eg: 3.5 
    num_credits_hours int,
    faculty varchar(100)
);

-- Adding linker tables
drop table if exists instructor_has_course;
create table instructor_has_course(
	instructor_id int,
    course_code varchar(20),
    location varchar(100),
    timetable varchar (100),
    primary key (instructor_id, course_code)
);

drop table if exists Student_has_course;
create table Student_has_course(
	student_id int,
    course_code varchar(20),
    primary key (student_id, course_code)
);

drop table if exists Student_has_Assignmet;
create table Student_has_Assignment(
	student_id int,
    course_code varchar(20),
    assignment_number int,
    primary key (student_id, course_code)
);

